    private void bsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsearchActionPerformed
        // TODO add your handling code here:
        tabledetails();
        int checkid=0;
        String id = txtmid.getText();
        String month=txtdate.getText();
        try
        {
            Connection con = ConProv.Getcon();
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery("select *from Member where MID='"+id+"'");
            while(r.next())
            {
                checkid = 1;
                txtmid.setEditable(false);
                txtname.setText(r.getString(2));
                txtmob.setText(r.getString(3));
                txtemail.setText(r.getString(4));
                txtamount.setText(r.getString(11));
            }
            if(checkid==0)
                JOptionPane.showMessageDialog(this, "Member ID does not exist!");
            
            ResultSet r1 = s.executeQuery("select * from payment inner join Member where payment.month='"+month+"' and payment.ID='"+id+"' and Member.MID='"+id+"'");
            while(r1.next())
            {
                bsave.setVisible(false);
                JOptionPane.showMessageDialog(this, "Payment has already been made for this month!");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_bsearchActionPerformed

