    public void tabledetails()
    {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        String id = txtmid.getText();
        try
        {
            Connection con = ConProv.Getcon();
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery("select * from payment where id='"+id+"'");
            while(r.next())
            {
                model.addRow(new Object[]{r.getString(2),r.getString(3)});
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex);
        }
    }
    
